package se.kanisha.dicegame;

public class PlayerInfo
{ // Class starts.

    String playerName;



    public PlayerInfo(String playerName)
    {
        this.playerName = playerName;
    }



    public String getPlayerName()
    {
        return playerName;
    }


}  // Class closes.


